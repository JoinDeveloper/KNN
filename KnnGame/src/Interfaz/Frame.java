  private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    double opcion1=0, opcion2=0, opcion3=0, opcion4=0, k=0;
    opcion1 = jComboBox1.getSelectedIndex()+1;
    opcion2 = jComboBox2.getSelectedIndex()+1;
    opcion3 = jComboBox3.getSelectedIndex()+1;
    opcion4 = jComboBox4.getSelectedIndex()+1;
    k = jComboBox5.getSelectedIndex()+1;
    manejador = new ManejadorMatriz();
    arreglo = new ArrayList<Contenedor>();
    int i = 0, contadorSi = 0, contadorNo = 0;
    String cadena= "";
    System.out.println(opcion1+"-"+opcion2+"-"+opcion3+"-"+opcion4);
    
    try{
      manejador.cargaMatrizDesdeArchivo("src/TrainingSet/juego.txt", opcion1,opcion2,opcion3,opcion4, arreglo);
    }catch(IOException ioe){
      System.out.println("Error de carga: "+ioe.getMessage());
    }//end try-catch
    
    System.out.println("La probabilidad es: ");
    Collections.sort(arreglo,Collections.reverseOrder());
    
    for (Contenedor con : arreglo) {
      i++;
      if(i > arreglo.size()-k){
        System.out.println("Clase: "+con.getClase()+" y distancia: "+con.getDistancia());
        cadena += "Clase: "+con.getClase() +"\tDistancia: "+con.getDistancia()+"\n";
        if(con.getClase().equals("SI")){
          contadorSi++;
        }else{
         contadorNo++; 
        }//end if-else
      }//end if
    }//end for
    
    jTextArea1.setText(cadena);
    jTextArea1.setText(cadena);
    
    if(contadorSi>contadorNo){
      JOptionPane.showMessageDialog(null,"Si se Juega");
    }else{
      JOptionPane.showMessageDialog(null,"No se Juega");
    }//end if-else
  }//GEN-LAST:event_jButton1ActionPerformed

