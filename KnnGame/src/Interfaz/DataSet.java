  public void cargarDatos(String rutaArchivo) throws IOException{
    String [] titulos = {"Panorama","Temperatura","Humedad","Viento","Clase Final"};
    String [] registros = new String[6];
    model = new DefaultTableModel(null,titulos);

    String linea = "";
    BufferedReader in = new BufferedReader(new FileReader(rutaArchivo));
    
    while(((linea = in.readLine()) != null)){
      //this.parseFilaMatriz(linea);
      String partes[] = linea.split("#");
      registros[0] = partes[0];
      registros[1] = partes[1];
      registros[2] = partes[2];
      registros[3] = partes[3];
      registros[4] = partes[4];
      model.addRow(registros);
    } //end while
    jTable1.setModel(model);
  }//end cargar Datos

